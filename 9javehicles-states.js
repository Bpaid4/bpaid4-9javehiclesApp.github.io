  const sectionCenter = document.querySelector(".section-center");
  const btnContainer = document.querySelector(".btn-container");
  // display all items when page loads
  window.addEventListener("DOMContentLoaded", function () {
    diplayStatepItems(statep);
    displayStatepButtons();
  });
  
  function diplayStatepItems(statepItems) {
    let displayStatep = statepItems.map(function (item) {
      // console.log(item);
  
      return `<article class="statep-item">
            <img src=${item.img} alt=${item.title} class="photo" />
            <div class="item-info">
              <header>
                <h4>${item.title}</h4>
                <h4 class="lga">${item.lga}</h4>
              </header>
              <p class="item-text">
                ${item.desc}
              </p>
            </div>
          </article>`;
    });
    displayStatep = displayStatep.join("");
    // console.log(displayStatep);
  
    sectionCenter.innerHTML = displayStatep;
  }
  function displayStatepButtons() {
    const categories = statep.reduce(
      function (values, item) {
        if (!values.includes(item.category)) {
          values.push(item.category);
        }
        return values;
      },
      ["all"]
    );
    const categoryBtns = categories
      .map(function (category) {
        return `<button type="button" class="filter-btn" data-id=${category}>
            ${category}
          </button>`;
      })
      .join("");
  
    btnContainer.innerHTML = categoryBtns;
    const filterBtns = btnContainer.querySelectorAll(".filter-btn");
    console.log(filterBtns);
  
    filterBtns.forEach(function (btn) {
      btn.addEventListener("click", function (e) {
        // console.log(e.currentTarget.dataset);
        const category = e.currentTarget.dataset.id;
        const statepCategory = statep.filter(function (statepItem) {
          // console.log(statepItem.category);
          if (statepItem.category === category) {
            return statepItem;
          }
        });
        if (category === "all") {
          diplayStatepItems(statep);
        } else {
          diplayStatepItems(statepCategory);
        }
      });
    });
  }
  